package me.droreo002.oreofishing.objects;

public class Placeholder {

    private String from;
    private String to;

    public Placeholder(String from, String to) {
        this.from = from;
        this.to = to;
    }

    public String getFrom() {
        return from;
    }

    public String getTo() {
        return to;
    }
}
